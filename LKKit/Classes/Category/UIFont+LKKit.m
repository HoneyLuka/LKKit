#import "UIFont+LKKit.h"

@implementation UIFont (LKKit)

@end

UIFont * LKAvenirMediumFont(CGFloat fontSize) {
    return [UIFont fontWithName:@"Avenir-Medium" size:fontSize];
}

UIFont * LKAvenirLightFont(CGFloat fontSize) {
    return [UIFont fontWithName:@"Avenir-Light" size:fontSize];
}
